<script>
//to force the column directed flexbox to wrap, the container needs a fixed height. so we give the box half of the height for 2 column layout. (I choose column instead of row to archive the wanted count direction)
const subnvi = $( ".top-bar .top-bar_right .navigation ul ul" );

var breakPointSwitch = 0; //because the windowWidth and css mediaquerie doesnt fit in all browsers, I check for an element, controlled by a media querie. So the css and JS funciton is fired always at the same time in all browsers.

function curWid(){      
    if($('.logo:visible').length != 0 && breakPointSwitch === 0 ) { 
        breakPointSwitch = 1;        
        //console.log(breakPointSwitch);
            $('.top-bar .top-bar_right .navigation ul ul').each(function() {
            var $this = $(this);
            var halfSubnaviMenuHeight = $this.outerHeight()/2+40;
                $this.css("height", halfSubnaviMenuHeight);
            });
    } else if($('.logo:visible').length == 0 && breakPointSwitch === 1 ) {
        breakPointSwitch = 0;
        //console.log(breakPointSwitch);
        $(subnvi).css("height", "auto");
    }
}
curWid();

$( window ).resize(function() {
    curWid();
});

/*mobile accordion stuff*/
$('.navigation > ul > li').each(function() {
    var $this = $(this);
        $this.click( function(){
            //$('.navigation > ul > li').removeClass('subnavi-active');
            $('.navigation > ul > li').not($(this)).removeClass('subnavi-active');
            $this.toggleClass('subnavi-active');
        });
});
